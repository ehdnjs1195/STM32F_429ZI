/* USER CODE BEGIN 0 */
int scaling(int x) //가변저항 값의 duty ratio를 0~100으로 조정하는 함수
  {
	  return (x * 2000) / 1023;
  }

int scalingRatio(int x) //모터의 각도를 0도부터 180도까지 조정하는 함수
{
	int t = (250 * x)/10;
	if(t < 50) return 50;
	return t;
}
