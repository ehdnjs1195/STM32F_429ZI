/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  MX_USART3_UART_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  MX_TIM4_Init();

  /* Initialize interrupts */
  MX_NVIC_Init();
  /* USER CODE BEGIN 2 */
  HAL_ADC_Start_IT(&hadc1);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_3);	//PWM3 of TIM3 start => 가변저항 PWM
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);	//PWM2 of TIM3 start => 서보모터 PWM
//  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);	//PWM3 of TIM4 start BUZZ
  HAL_TIM_Base_Start_IT(&htim2);
  LCD_Init(LCD_ADDR);
  init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  HAL_ADC_Start_IT(&hadc1);
	  TIM3 -> CCR3 = scaling(adc_value);
	  TIM3 -> CCR2 = scalingRatio(ratio);
	  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_11, scaling(adc_value)); //외부 LED / pull-up저항으로 연결

	  printf("PWM CH3 value: %d, Scaling value: %d, PWM CH2 value: %d, Scaling value: %d \r\n", adc_value, scaling(adc_value), ratio, scalingRatio(ratio));
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  sprintf(ADCstring, "ADC value : %04d    ", scaling(adc_value));
	  sprintf(MS_Ratio, "MS Ratio : %04d     ", scalingRatio(ratio));

	  if(lcdPrint == 1) //0.1초마다 TIM2에 의해 lcdPrint가 1로 바뀌어 LCD를 출력함
	  {
		  LCD_SendCommand((0x27 << 1), 0b10000000); //0x00
		  LCD_SendString((0x27 << 1), ADCstring);
		  LCD_SendCommand((0x27 << 1), 0b11000000); //0x00
		  LCD_SendString((0x27 << 1), MS_Ratio);
		  lcdPrint = 0;
	  }
  }
  /* USER CODE END 3 */
}

